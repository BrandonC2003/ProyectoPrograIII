    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Productos producto = new Productos();

        producto.setNombreProducto(txtProducto.getText());
        producto.setCantidad(Integer.parseInt(spnStock.getValue().toString()));
        producto.setPrecioUnitario(Double.parseDouble(spnPrecio.getValue().toString()));
        producto.setDescuento(Double.parseDouble(spnDescuento.getValue().toString()));
        int index = cboCategoria.getSelectedIndex();

        boolean resultado = cliente.insertarProductos(valor.get(index), producto.getNombreProducto(), producto.getCantidad(), producto.getPrecioUnitario(), producto.getDescuento(), FormLogin.nombreUsuario);

        if (resultado) {
            JOptionPane.showMessageDialog(this, "Producto insertado exitosamente");
            limpiar();
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Ocurrio un error al intentar insertar el producto");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

