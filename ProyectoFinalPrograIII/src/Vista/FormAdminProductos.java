    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Productos producto = new Productos();

        producto.setNombreProducto(txtProducto.getText());
        producto.setCantidad(Integer.parseInt(spnStock.getValue().toString()));
        producto.setPrecioUnitario(Double.parseDouble(spnPrecio.getValue().toString()));
        producto.setDescuento(Double.parseDouble(spnDescuento.getValue().toString()));
        int index = cboCategoria.getSelectedIndex();

        if (idProd > 0) {
            boolean resultado = cliente.modificarProducto(idProd, valor.get(index), producto.getNombreProducto(), producto.getCantidad(), producto.getPrecioUnitario(), producto.getDescuento(), FormLogin.nombreUsuario);

            if (resultado) {
                JOptionPane.showMessageDialog(this, "Producto modificado exitosamente");
                limpiar();
                llenarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Ocurrio un error al actualizar insertar el producto");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debes seleccionar un producto para poder editarlo");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

